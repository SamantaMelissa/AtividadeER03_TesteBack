namespace Matematica
{
    public class Operacoes
    {
        //Método de soma:
        public static double Somar(double number01, double number02)
        {
            return number01 + number02;
        }

        public static double Subtrair(double number01, double number02)
        {
            return number01 - number02;
        }

        public static double Multiplicar(double number01, double number02)
        {
            return number01 * number02;
        }

    }
}